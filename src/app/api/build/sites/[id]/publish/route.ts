import { NextResponse } from "next/server";
import { z } from "zod";
import { Octokit } from "@octokit/rest";
import { requireUser } from "@/lib/supabase/server";

export const runtime = "nodejs";
export const maxDuration = 60;

const Body = z.object({
  repoName: z
    .string()
    .min(1)
    .max(100)
    .regex(/^[a-zA-Z0-9._-]+$/, "letters, numbers, dot, dash, underscore only"),
  enablePages: z.boolean().default(true),
});

export async function POST(
  req: Request,
  ctx: { params: Promise<{ id: string }> }
) {
  let supabase;
  try {
    ({ supabase } = await requireUser());
  } catch {
    return NextResponse.json({ error: "unauthorized" }, { status: 401 });
  }

  const token = process.env.GITHUB_TOKEN;
  if (!token) {
    return NextResponse.json(
      { error: "GITHUB_TOKEN not configured on the server" },
      { status: 500 }
    );
  }

  const parsed = Body.safeParse(await req.json());
  if (!parsed.success) {
    return NextResponse.json(
      { error: parsed.error.issues[0]?.message ?? "bad request" },
      { status: 400 }
    );
  }
  const { repoName, enablePages } = parsed.data;
  const { id } = await ctx.params;

  const { data: site, error: sErr } = await supabase
    .from("sites")
    .select("id,title,html")
    .eq("id", id)
    .single();
  if (sErr || !site) {
    return NextResponse.json({ error: "site not found" }, { status: 404 });
  }

  const octo = new Octokit({ auth: token });

  let owner: string;
  try {
    const { data: me } = await octo.users.getAuthenticated();
    owner = me.login;
  } catch (e) {
    return NextResponse.json(
      { error: "GitHub token invalid or missing scopes: " + (e as Error).message },
      { status: 502 }
    );
  }

  // Create the repo (public, with auto-init so we have a default branch).
  let defaultBranch = "main";
  try {
    const { data: repo } = await octo.repos.createForAuthenticatedUser({
      name: repoName,
      description: `Generated by Jarvis — ${site.title}`,
      private: false,
      auto_init: true,
    });
    defaultBranch = repo.default_branch ?? "main";
  } catch (e) {
    const msg = (e as Error).message ?? "";
    if (msg.includes("already exists")) {
      return NextResponse.json(
        { error: `Repo "${repoName}" already exists on your GitHub. Pick a different name.` },
        { status: 409 }
      );
    }
    return NextResponse.json({ error: "Failed to create repo: " + msg }, { status: 502 });
  }

  // Write index.html (and a tiny README) to the new repo.
  const html = site.html;
  const readme = `# ${site.title}\n\nGenerated by [Jarvis](https://github.com/) on ${new Date().toISOString().slice(0, 10)}.\n\nOpen \`index.html\` in a browser, or visit the GitHub Pages URL once it builds.\n`;

  try {
    // Read the auto-init README sha so we can overwrite it.
    let readmeSha: string | undefined;
    try {
      const existing = await octo.repos.getContent({ owner, repo: repoName, path: "README.md" });
      if (!Array.isArray(existing.data) && "sha" in existing.data) {
        readmeSha = existing.data.sha;
      }
    } catch {
      // no existing readme — that's fine
    }

    await octo.repos.createOrUpdateFileContents({
      owner,
      repo: repoName,
      path: "index.html",
      message: "Initial site from Jarvis",
      content: Buffer.from(html, "utf-8").toString("base64"),
      branch: defaultBranch,
    });
    await octo.repos.createOrUpdateFileContents({
      owner,
      repo: repoName,
      path: "README.md",
      message: "Add README",
      content: Buffer.from(readme, "utf-8").toString("base64"),
      branch: defaultBranch,
      sha: readmeSha,
    });
  } catch (e) {
    return NextResponse.json(
      { error: "Failed to push files: " + (e as Error).message },
      { status: 502 }
    );
  }

  let pagesUrl: string | undefined;
  if (enablePages) {
    try {
      await octo.repos.createPagesSite({
        owner,
        repo: repoName,
        source: { branch: defaultBranch, path: "/" },
      });
      pagesUrl = `https://${owner}.github.io/${repoName}/`;
    } catch (e) {
      // Pages might already be enabled or scope missing — non-fatal.
      console.warn("[publish] pages enable failed:", (e as Error).message);
    }
  }

  return NextResponse.json({
    ok: true,
    repoUrl: `https://github.com/${owner}/${repoName}`,
    owner,
    repoName,
    pagesUrl,
  });
}
